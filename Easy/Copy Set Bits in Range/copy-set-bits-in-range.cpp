// User function Template for C++
class Solution{
    public:
    int setSetBit(int x, int y, int l, int r){
        int c=1;
        while(y)
        {
            if(y&1)
            {
                if(c>=l and c<=r)
                {
                    x=x | 1<<(c-1);
                }
            }
            y=y>>1;
            c++;
            
        }
        return x;
    }
};

